//Programas sobre manejo de memoria
#include<stdio.h> //En vez de include<iostream>

int main(){
    int x=3;
    int salir;
    printf("Pepito mide %i metros",x); //Esto equivale al "cout<<". Tiene dos partes: tipo de variable que queremos escribir, separa una coma, y lo segundo son las variables
    // %i es un int , %c es char 
    //lo que ponemos antes de " %i " sera lo que salga por pantalla 
    scanf("%c",&salir); //Esto equivale al cin        
}
